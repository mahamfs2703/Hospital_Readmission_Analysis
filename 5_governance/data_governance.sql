----------------------------------------------------------------------
-- 1. SHARED UNMASK FUNCTION (Split Pattern)
----------------------------------------------------------------------
CREATE OR REPLACE FUNCTION HEALTHCARE_DB.GOVERNANCE.UNMASK_PII()
RETURNS BOOLEAN
MEMOIZABLE
AS
$$
    CURRENT_ROLE() IN ('ACCOUNTADMIN', 'ADMIN_ROLE', 'DATA_ENGINEER_ROLE')
$$;

CREATE OR REPLACE FUNCTION HEALTHCARE_DB.GOVERNANCE.UNMASK_SENSITIVE()
RETURNS BOOLEAN
MEMOIZABLE
AS
$$
    CURRENT_ROLE() IN ('ACCOUNTADMIN', 'ADMIN_ROLE', 'DATA_ENGINEER_ROLE', 'DATA_ANALYST_ROLE')
$$;

----------------------------------------------------------------------
-- 2. MASKING POLICIES
----------------------------------------------------------------------

CREATE OR REPLACE MASKING POLICY HEALTHCARE_DB.GOVERNANCE.MASK_STRING_PII
AS (val VARCHAR)
RETURNS VARCHAR ->
    CASE
        WHEN HEALTHCARE_DB.GOVERNANCE.UNMASK_PII() THEN val
        ELSE '***MASKED***'
    END
COMMENT = 'Masks string PII (names, addresses). Full access: ACCOUNTADMIN, ADMIN_ROLE, DATA_ENGINEER_ROLE.';

CREATE OR REPLACE MASKING POLICY HEALTHCARE_DB.GOVERNANCE.MASK_PHONE
AS (val VARCHAR)
RETURNS VARCHAR ->
    CASE
        WHEN HEALTHCARE_DB.GOVERNANCE.UNMASK_PII() THEN val
        ELSE CONCAT('***-***-', RIGHT(val, 4))
    END
COMMENT = 'Partially masks phone numbers showing last 4 digits.';

CREATE OR REPLACE MASKING POLICY HEALTHCARE_DB.GOVERNANCE.MASK_ZIP_CODE
AS (val VARCHAR)
RETURNS VARCHAR ->
    CASE
        WHEN HEALTHCARE_DB.GOVERNANCE.UNMASK_SENSITIVE() THEN val
        ELSE LEFT(val, 3) || '**'
    END
COMMENT = 'Masks ZIP code to 3-digit prefix for geographic generalization.';

CREATE OR REPLACE MASKING POLICY HEALTHCARE_DB.GOVERNANCE.MASK_FINANCIAL_VALUE
AS (val FLOAT)
RETURNS FLOAT ->
    CASE
        WHEN HEALTHCARE_DB.GOVERNANCE.UNMASK_SENSITIVE() THEN val
        ELSE NULL
    END
COMMENT = 'Masks financial metric values. Returns NULL for unauthorized roles.';

CREATE OR REPLACE MASKING POLICY HEALTHCARE_DB.GOVERNANCE.MASK_CLINICAL_METRIC
AS (val FLOAT)
RETURNS FLOAT ->
    CASE
        WHEN HEALTHCARE_DB.GOVERNANCE.UNMASK_SENSITIVE() THEN val
        ELSE ROUND(val, 0)
    END
COMMENT = 'Reduces precision of clinical metrics for unauthorized roles.';

CREATE OR REPLACE MASKING POLICY HEALTHCARE_DB.GOVERNANCE.MASK_CLINICAL_METRIC_VARCHAR
AS (val VARCHAR)
RETURNS VARCHAR ->
    CASE
        WHEN HEALTHCARE_DB.GOVERNANCE.UNMASK_SENSITIVE() THEN val
        ELSE '***'
    END
COMMENT = 'Masks varchar clinical metrics for unauthorized roles.';

