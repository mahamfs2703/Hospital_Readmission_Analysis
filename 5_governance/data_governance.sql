----------------------------------------------------------------------
-- 1. ROW ACCESS POLICY
-- Restricts data visibility by state based on role-to-region mapping
----------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS HEALTHCARE_DB.GOVERNANCE.STATE_ACCESS_MAPPING (
    ROLE_NAME     VARCHAR,
    ALLOWED_STATE VARCHAR,
    CONSTRAINT pk_state_access PRIMARY KEY (ROLE_NAME, ALLOWED_STATE)
);

INSERT INTO HEALTHCARE_DB.GOVERNANCE.STATE_ACCESS_MAPPING
    (ROLE_NAME, ALLOWED_STATE)
VALUES
    ('ACCOUNTADMIN',       'ALL'),
    ('ADMIN_ROLE',         'ALL'),
    ('DATA_ENGINEER_ROLE', 'ALL'),
    ('DATA_ANALYST_ROLE',  'ALL'),
    ('DATA_INGEST_ROLE',   'ALL'),
    ('REPORTING_ROLE',     'AL'),
    ('REPORTING_ROLE',     'CA'),
    ('REPORTING_ROLE',     'NY'),
    ('REPORTING_ROLE',     'TX'),
    ('REPORTING_ROLE',     'FL');

CREATE OR REPLACE ROW ACCESS POLICY HEALTHCARE_DB.GOVERNANCE.RAP_STATE_FILTER
AS (state_col VARCHAR)
RETURNS BOOLEAN ->
    EXISTS (
        SELECT 1
        FROM HEALTHCARE_DB.GOVERNANCE.STATE_ACCESS_MAPPING
        WHERE IS_ROLE_IN_SESSION(ROLE_NAME)
          AND (ALLOWED_STATE = state_col OR ALLOWED_STATE = 'ALL')
    );

-- APPLY ROW ACCESS POLICY
